    private void jbtnDiscountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDiscountActionPerformed
        
        
        try{
        
            String value = jtxtDiscount.getText();
            Connection sqlConn = ConnectionProvider.getCon();
            String sqlQuestion = "Select count(ClientId) from clients where ClientId =" + value;
            pst = sqlConn.prepareStatement(sqlQuestion);
            rs = pst.executeQuery();
            
            if (rs.next()){
            
            String countClientId = rs.getString("count(ClientId)");
            jSessionsCounts.setText(countClientId);
            
            int i = Integer.parseInt(countClientId);
            
            switch (i){
           
                case 0,1: jLabelDiscount.setText("0");
                break;
                case 2: jLabelDiscount.setText("3");
                break;
                case 3,4,5: jLabelDiscount.setText("5");
                break;
                case 6,7,8: jLabelDiscount.setText("7");
                break;
                default:
                    jLabelDiscount.setText("10");
            
            }
            
            }
            
            
 
        }
        catch(Exception e){
        
             JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jbtnDiscountActionPerformed

