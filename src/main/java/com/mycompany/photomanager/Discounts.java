    public void upDateDiscounts()
    {
        try
        {
            Connection sqlConn = ConnectionProvider.getCon();
            pst = sqlConn.prepareStatement("Select clientId, name , surname from clients");
            
            rs = pst.executeQuery();
            ResultSetMetaData stData = rs.getMetaData();
            
            q = stData.getColumnCount();
            
            DefaultTableModel RecordTable = (DefaultTableModel)jTableDiscounts.getModel();
                    RecordTable.setRowCount(0);
                    
                    while(rs.next()){
                    Vector columnData = new Vector();
                    
                    for(i = 1; i <= q; i++)
                    {
                      
                        columnData.add(rs.getString("ClientId"));
                        columnData.add(rs.getString("Name"));
                        columnData.add(rs.getString("Surname"));
                        

                           
                    }
                    RecordTable.addRow(columnData);
     
                    }
        }
        catch (Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        
        }
    
    
    }
    
    
